    private void tblhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblhActionPerformed
        // TODO add your handling code here:
    if (Integer.parseInt(ja.getText())>=3) {
        int gajih=Integer.parseInt(gj.getText());
        int ttl=(gajih+gajih*10/100*3);
        gjt.setText("Rp. "+ttl);
    }
    else{
       int ttll=Integer.parseInt(gj.getText());
       int bc=Integer.parseInt(ja.getText());
       int ttl=(ttll)+(ttll*10/100*bc);
       gjt.setText("Rp. "+ttl);
    }
    }//GEN-LAST:event_tblhActionPerformed

