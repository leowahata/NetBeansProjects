    private void cbmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbmActionPerformed
        // TODO add your handling code here:
    if (cbm.isSelected()) {
            if (sksw.isSelected()) {
            p.setText("Nama Suami");
            nmp.setEditable(true);
            nmp.setText("");
            }else if (sksp.isSelected()) {
            p.setText("Nama Istri");
            nmp.setEditable(true);
            nmp.setText("");
            }else {
            nmp.setEditable(false);
    }
    ja.setEditable(true);
    ja.setText("");
    } else {
    ja.setEditable(false);
    p.setText("Belum Menikah");
    ja.setText("0");
    }
    }//GEN-LAST:event_cbmActionPerformed

